package com.huawei.classroom.student.h57;


public class Pair {
	private int num,day;
	public Pair(int num,int day) {
		this.num=num;
		this.day=day;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	
}
